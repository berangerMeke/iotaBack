import { Component, OnInit } from '@angular/core';
import { ActivatedRoute } from '@angular/router';
import { JhiDataUtils } from 'ng-jhipster';

import { IObjectifs } from 'app/shared/model/objectifs.model';

@Component({
  selector: 'jhi-objectifs-detail',
  templateUrl: './objectifs-detail.component.html'
})
export class ObjectifsDetailComponent implements OnInit {
  objectifs: IObjectifs | null = null;

  constructor(protected dataUtils: JhiDataUtils, protected activatedRoute: ActivatedRoute) {}

  ngOnInit(): void {
    this.activatedRoute.data.subscribe(({ objectifs }) => (this.objectifs = objectifs));
  }

  byteSize(base64String: string): string {
    return this.dataUtils.byteSize(base64String);
  }

  openFile(contentType: string, base64String: string): void {
    this.dataUtils.openFile(contentType, base64String);
  }

  previousState(): void {
    window.history.back();
  }
}
